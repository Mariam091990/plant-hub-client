import React, { use, useState } from 'react';
import { AuthContext } from '../../provider/AuthProvider';
import Swal from 'sweetalert2';
import { Link, useLocation, useNavigate } from 'react-router';

const Login = () => {


    const { signIn, googleSignIn } = use(AuthContext);
    const location = useLocation();
    const navigate = useNavigate();

    const [error, setError] = useState("");
    const [email, setEmail] = useState("");


    const handleLogin = (e) => {

        e.preventDefault();

        const form = e.target;
        const email = form.email.value;
        const password = form.password.value;

        signIn(email, password)
            .then((result) => {


                Swal.fire({
                    position: "top-end",
                    icon: "success",
                    title: "Logged in successfully",
                    showConfirmButton: false,
                    timer: 1500
                });

                const user = result.user
                console.log(user);
                navigate(location.state ? location.state : "/")


            })


            .catch((error) => {
                const errorCode = error.code;


                setError(errorCode);

            })


    }

    const handleGoogleLogIn = () => {

        googleSignIn()
            .then((result) => {
                const user = result.user
                console.log(user);


            })

            .catch((error) => {
                const errorCode = error.code;
                const errorMessage = error.message;
                alert(errorCode, errorMessage);
            })






    }


    const handleForgetPassword = () => {

        navigate("/auth/forgetPass", { state: { email } });

    }


    return (
        <div>
            <div className="hero bg-base-200 min-h-screen">
                <div className="hero-content flex-col  gap-10 ">
                    <div className="text-center ">
                        <h1 className="text-5xl font-bold">Login now!</h1>

                    </div>
                    <div className="card bg-base-100 w-full max-w-sm shrink-0 shadow-2xl">
                        <div className="card-body">
                            <form onSubmit={handleLogin} className="fieldset">

                                {/* Email */}
                                <label className="label">Email</label>

                                <input
                                    name='email'
                                    type="email"
                                    className="input"
                                    value={email} onChange={(e) => setEmail(e.target.value)}

                                    placeholder="Email" />

                                {/* password */}
                                <label className="label">Password</label>
                                <input
                                    name="password"
                                    type="password"
                                    className="input"

                                    placeholder="Password" />


                                <div><button type='button' onClick={handleForgetPassword} className="text-blue-600 underline text-sm link link-hover" >Forgot password?</button></div>
                               
                                {error && <p className='text-red-600 mt-3'>{error}</p>}

                                <div> <button type='submit' className="btn btn-neutral w-full mt-4">Login</button></div>

                                <p>New to this site? please <Link className='text-blue-800' to="/auth/register">Register</Link> </p>

                                <button onClick={handleGoogleLogIn} className="btn bg-white text-black border-[#e5e5e5]">
                                    <svg aria-label="Google logo" width="16" height="16" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512"><g><path d="m0 0H512V512H0" fill="#fff"></path><path fill="#34a853" d="M153 292c30 82 118 95 171 60h62v48A192 192 0 0190 341"></path><path fill="#4285f4" d="m386 400a140 175 0 0053-179H260v74h102q-7 37-38 57"></path><path fill="#fbbc02" d="m90 341a208 200 0 010-171l63 49q-12 37 0 73"></path><path fill="#ea4335" d="m153 219c22-69 116-109 179-50l55-54c-78-75-230-72-297 55"></path></g></svg>
                                    Login with Google
                                </button>

                            </form>

                        </div>
                    </div>
                </div>
            </div>
        </div>
    );
};

export default Login;